    public void Add_data()
    {
        
        String item_ID = itemID.getText();
        
        String item_Name = itemName.getText();
        
        String item_Brand = itemBrand.getText();
        
        String item_Size = itemSize.getText();
        
        String item_Category = itemCategory.getText();
        
        String item_Price = itemPrice.getText();
        
        String cus_Price = custom_price.getText();
        
        java.sql.Connection connection = Connector.connection();
        
        String SQL = "INSERT INTO grocery_items VALUES ('"+item_ID+"','"+item_Name+"','"+item_Brand+"','"+item_Size+"','"+item_Category+"','"+item_Price+"','"+cus_Price+"')";
        
        
        try
        {
           PreparedStatement preparedStatement = connection.prepareStatement(SQL);

           preparedStatement.executeUpdate();
           
           System.out.println("Data Insert Successfully !!");
           
           JOptionPane.showMessageDialog(null,"Item Added to the System Successfully !!");

        }
        catch(Exception error)
        {
            System.out.println("ERROR CODE : "+error);
            
            JOptionPane.showMessageDialog(null,"Attempt Unsuccessful");
        }
        
        
       
        
        
        
    }
    
