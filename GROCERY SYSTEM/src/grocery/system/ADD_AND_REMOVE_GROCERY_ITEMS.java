        String item_Price = itemPrice.getText();
        
        String cus_Price = custom_price.getText();
        
        
        java.sql.Connection connection = Connector.connection();
        
        String SQL = "DELETE FROM grocery_items WHERE ITEM_ID = '"+item_ID+"' ";
        
